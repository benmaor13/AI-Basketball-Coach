from pydantic import BaseModel, Field
from typing import List, Literal

class TacticalAction(BaseModel):
    """
    Represents a single, concrete tactical recommendation from the AI.
    """

    action_type: Literal["Substitution", "Timeout", "Defensive Shift", "Pace Adjustment" ] = Field(
        ...,
        description="The category of the recommended move"
    )

    description: str = Field(
        ...,
        description="Clear instructions for the coach",
        json_schema_extra={"example": "Sub out Player #13 for better defense"}
    )

    priority: Literal["High", "Medium", "Low"] = Field(
        default="Medium",
        description="How urgent this action is given the current game momentum"
    )

    expected_impact: str = Field(
        ...,
        description="The strategic reason/benefit of this action",
        json_schema_extra = {"example": "the best player of the other team will score less"}
    )

class AnalysisReport(BaseModel):
    """
    The final structured response generated by the AI Coach.
    """
    summary: str = Field(
        ...,
        description="A tactical overview of the current game situation"
    )

    main_threat: str = Field(
        ...,
        description="The most critical challenge identified"
    )

    recommended_actions: List[TacticalAction] = Field(
        ...,
        min_length=1,
        description="A prioritized list of moves the coach should consider"
    )

    risk_assessment: str = Field(
        ...,
        description="Potential downsides of the proposed strategy"
    )

    confidence_score: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="AI's certainty level in this specific analysis (0 to 1)"
    )

    class Config:
        # better presentation of the example in the swagger
        json_schema_extra = {
            "example": {
                "summary": "Opponent is on a 10-0 run due to fatigue in our backcourt.",
                "main_threat": "Transition defense failure",
                "recommended_actions": [
                    {
                        "action_type": "Timeout",
                        "description": "Call timeout to rest starters.",
                        "priority": "High",
                        "expected_impact": "Stop the momentum and reorganize the defense."
                    }
                ],
                "risk_assessment": "Calling a timeout now leaves only 1 remaining for the quarter.",
                "confidence_score": 0.92
            }
        }