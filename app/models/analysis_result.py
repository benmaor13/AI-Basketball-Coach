from pydantic import BaseModel, Field, ConfigDict
from typing import List, Literal, Optional

class TacticalAction(BaseModel):
    """
    Represents a single, concrete tactical recommendation from the AI.
    """
    # Required Fields
    action_type: Literal["Substitution", "Timeout", "Defensive Shift", "Pace Adjustment", "Playcall"] = Field(
        ...,
        description="The tactical category of the move. Options: 'Substitution', 'Timeout', 'Defensive Shift', 'Pace Adjustment', 'Playcall'.",
        json_schema_extra={"example": "Substitution"}
    )

    description: str = Field(
        ...,
        description="Clear, concise instruction for the coach (e.g., 'Sub out #13 for #22').",
        json_schema_extra={"example": "Sub out Player #13 and bring in #22 for interior size."}
    )

    expected_impact: str = Field(
        ...,
        description="The immediate tactical benefit expected from this move.",
        json_schema_extra={"example": "Will improve rim protection and defensive rebounding."}
    )

    # Optional Fields
    priority: Literal["High", "Medium", "Low"] = Field(
        default="Medium",
        description="Urgency level based on the game situation. Options: 'High', 'Medium', 'Low'.",
        json_schema_extra={"example": "High"}
    )

    involved_player_numbers: List[int] = Field(
        default_factory=list,
        description="Jersey numbers of players involved. For substitutions, first is OUT, second is IN.",
        json_schema_extra={"example": [13, 22]}
    )


class AnalysisReport(BaseModel):
    """
    The final structured response generated by the AI Coach.
    """
    #  Required Fields
    summary: str = Field(
        ...,
        description="Concise overview of current game flow.",
        json_schema_extra={"example": "Opponent is on a 10-0 run; our defense is collapsing."}
    )

    main_threat: str = Field(
        ...,
        description="The most critical challenge identified.",
        json_schema_extra={"example": "Opponent's center is dominating the offensive glass."}
    )

    recommended_actions: List[TacticalAction] = Field(
        ...,
        min_length=1,
        description="A prioritized list of concrete moves to counter the threat."

    )

    risk_assessment: str = Field(
        ...,
        description="Analysis of potential downsides or trade-offs.",
        json_schema_extra={"example": "Our lineup will be slower, potentially hurting transition offense."}
    )

    confidence_score: float = Field(
        ...,
        ge=0.0,
        le=1.0,
        description="AI's certainty in this analysis (0.0 to 1.0).",
        json_schema_extra={"example": 0.92}
    )

    # config for better experience in swagger and also for better AI performance
    model_config = ConfigDict(
        json_schema_extra={
            "example": {
                "summary": "Home team is leading by 6 with under 4 minutes left. The momentum is positive, but the away team's point guard is exploiting the drop coverage.",
                "main_threat": "Away team's #30 is scoring easily off high pick-and-rolls due to a lack of aggressive defensive pressure.",
                "recommended_actions": [
                    {
                        "action_type": "Defensive Shift",
                        "description": "Switch from drop coverage to blitzing the pick-and-roll against #30.",
                        "expected_impact": "Force the ball out of the primary scorer's hands and make their role players beat you.",
                        "priority": "High",
                        "involved_player_numbers": [23, 1]
                    },
                    {
                        "action_type": "Substitution",
                        "description": "Sub out tired big man #15 for quicker defender #8.",
                        "expected_impact": "Better mobility to execute the blitz and recover to the perimeter.",
                        "priority": "Medium",
                        "involved_player_numbers": [15, 8]
                    }
                ],
                "risk_assessment": "Blitzing will leave the roll man temporarily open, requiring perfect weak-side rotations to prevent easy layups.",
                "confidence_score": 0.92
            }
        }
    )